#include <stdio.h>

int main() {
    // soma: +; sub: -; mul: *; div: /; resto: %;
    int a = 10;
    int b = 3;
    printf("Soma:%d\n", a + b); //13
    printf("Resto:%d\n", a % b);//1
    printf("div:%d\n", a / b); //3
    printf("div:%d\n", a * b); //30
    printf("div:%d\n", a - b); //7
    
    return 0;
}
